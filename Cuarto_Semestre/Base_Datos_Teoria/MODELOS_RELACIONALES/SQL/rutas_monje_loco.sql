-- ==============================
-- CREAR TABLAS 
-- ==============================

-- RECORRIDO
CREATE TABLE IF NOT EXISTS RECORRIDO (
    ID_RECORRIDO INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE_RECORRIDO VARCHAR(100) NOT NULL,
    DURACION_HORAS INT NOT NULL,
    DESCRIPCION VARCHAR(200),   
    LISTAS_CIUDADES VARCHAR(200) NOT NULL
);

-- SERVICIO
CREATE TABLE IF NOT EXISTS SERVICIO (
    ID_SERVICIO INT AUTO_INCREMENT PRIMARY KEY,
    ID_RECORRIDO INT NOT NULL,
    NOMBRE_SERVICIO VARCHAR(100) NOT NULL,
    TIPO_SERVICIO VARCHAR(100) NOT NULL,
    DESCUENTO_ESTUDIANTE BOOLEAN NOT NULL DEFAULT FALSE,
    FOREIGN KEY (ID_RECORRIDO) REFERENCES RECORRIDO(ID_RECORRIDO)
);

-- VEHICULO
CREATE TABLE IF NOT EXISTS VEHICULO (
    ID_VEHICULO INT AUTO_INCREMENT PRIMARY KEY,
    PLACA VARCHAR(20) UNIQUE NOT NULL,
    MARCA VARCHAR(100) NOT NULL,
    MODELO VARCHAR(100) NOT NULL,
    ANIO INT NOT NULL,
    CAPACIDAD INT NOT NULL
);

-- ASIGNACION_VEHICULO
CREATE TABLE IF NOT EXISTS ASIGNACION_VEHICULO (    
    ID_ASIGNACION INT AUTO_INCREMENT PRIMARY KEY,
    ID_VEHICULO INT NOT NULL,
    ID_RECORRIDO INT NOT NULL,
    ID_SERVICIO INT NOT NULL,
    FECHA_ASIGNACION DATE NOT NULL,

    FOREIGN KEY(ID_VEHICULO) REFERENCES VEHICULO(ID_VEHICULO),
    FOREIGN KEY(ID_RECORRIDO) REFERENCES RECORRIDO(ID_RECORRIDO),
    FOREIGN KEY(ID_SERVICIO) REFERENCES SERVICIO(ID_SERVICIO)
);

-- HORARIO
CREATE TABLE IF NOT EXISTS HORARIO (
    ID_HORARIO INT AUTO_INCREMENT PRIMARY KEY,
    ID_RECORRIDO INT NOT NULL,
    FECHA_INICIO DATE NOT NULL,
    FECHA_FIN DATE NOT NULL,
    HORA_SALIDA TIME NOT NULL,
    HORA_LLEGADA TIME NOT NULL,

    FOREIGN KEY(ID_RECORRIDO) REFERENCES RECORRIDO(ID_RECORRIDO)
);

-- FUNCIONARIO
CREATE TABLE IF NOT EXISTS FUNCIONARIO (
    ID_FUNCIONARIO CHAR(5) PRIMARY KEY,
    CEDULA_FUNCIONARIO VARCHAR(20) UNIQUE NOT NULL,
    NOMBRE VARCHAR(100) NOT NULL,
    APELLIDO VARCHAR(100) NOT NULL,
    TELEFONO VARCHAR(15) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE,
    DIRECCION VARCHAR(200) NOT NULL,
    FECHA_NACIMIENTO DATE NOT NULL,
    CARGO VARCHAR(100) NOT NULL
);

-- CONDUCTOR
CREATE TABLE IF NOT EXISTS CONDUCTOR (
    ID_CONDUCTOR CHAR(5) PRIMARY KEY,
    ID_FUNCIONARIO CHAR(5) NOT NULL,
    LICENCIA VARCHAR(50) UNIQUE NOT NULL,
    ANIO_EXPEDICION INT NOT NULL,
    ANIO_VENCIMIENTO INT NOT NULL,
    KM_RECORRIDOS INT NOT NULL,

    FOREIGN KEY(ID_FUNCIONARIO) REFERENCES FUNCIONARIO(ID_FUNCIONARIO)
);

-- ASIGNACION_FUNCIONARIO
CREATE TABLE IF NOT EXISTS ASIGNACION_FUNCIONARIO (
    ID_ASIGNACION INT AUTO_INCREMENT PRIMARY KEY,
    ID_FUNCIONARIO CHAR(5) NOT NULL,
    ID_RECORRIDO INT NOT NULL,
    ID_SERVICIO INT NOT NULL,
    FECHA_ASIGNACION DATE NOT NULL,

    FOREIGN KEY(ID_FUNCIONARIO) REFERENCES FUNCIONARIO(ID_FUNCIONARIO),
    FOREIGN KEY(ID_RECORRIDO) REFERENCES RECORRIDO(ID_RECORRIDO),
    FOREIGN KEY(ID_SERVICIO) REFERENCES SERVICIO(ID_SERVICIO)
);

-- SUPLENTE
CREATE TABLE IF NOT EXISTS SUPLENTE (
    ID_SUPLENTE CHAR(5) PRIMARY KEY,
    ID_FUNCIONARIO CHAR(5),
    ID_CONDUCTOR CHAR(5),
    FECHA_INICIO DATE NOT NULL,
    FECHA_FIN DATE NOT NULL,

    FOREIGN KEY (ID_FUNCIONARIO) REFERENCES FUNCIONARIO(ID_FUNCIONARIO),
    FOREIGN KEY (ID_CONDUCTOR) REFERENCES CONDUCTOR(ID_CONDUCTOR),

    -- Regla: debe tener un funcionario o un conductor, pero no ambos
    CHECK (
        (ID_FUNCIONARIO IS NOT NULL AND ID_CONDUCTOR IS NULL) OR
        (ID_FUNCIONARIO IS NULL AND ID_CONDUCTOR IS NOT NULL)
    )
);




