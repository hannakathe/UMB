-- ==============================
-- CREAR TABLAS 
-- ==============================


-- CLIENTE
CREATE TABLE IF NOT EXISTS CLIENTE (
    CEDULA_CLIENTE VARCHAR(20) PRIMARY KEY, 
    NOMBRE VARCHAR(100) NOT NULL,
    APELLIDO VARCHAR(100) NOT NULL,
    CELULAR VARCHAR(15) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE, 
    DIRECCION VARCHAR(100) NOT NULL,
    FECHA_NACIMIENTO DATE
) ENGINE=InnoDB;

-- EMPLEADO
CREATE TABLE IF NOT EXISTS EMPLEADO(
    CODIGO_EMPLEADO CHAR(5) PRIMARY KEY,
    CEDULA_EMPLEADO VARCHAR(20) UNIQUE NOT NULL,
    NOMBRE VARCHAR(100) NOT NULL, 
    APELLIDO VARCHAR(100) NOT NULL,
    CELULAR VARCHAR(15) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE, 
    DIRECCION VARCHAR(100) NOT NULL,
    FECHA_NACIMIENTO DATE
) ENGINE=InnoDB;

-- CARGO
CREATE TABLE IF NOT EXISTS CARGO(
    NUMERO_CARGO VARCHAR(10) PRIMARY KEY,
    NOMBRE_CARGO VARCHAR(100) NOT NULL,
    RANGO_SALARIAL DECIMAL(10,2) NOT NULL
) ENGINE=InnoDB;

-- TIPO_PRODUCTO
CREATE TABLE IF NOT EXISTS TIPO_PRODUCTO(
    ID_TIPO_PRODUCTO VARCHAR(10) PRIMARY KEY,
    NOMBRE_TIPO_PRODUCTO VARCHAR(100)
) ENGINE=InnoDB;

-- FORMA_PAGO
CREATE TABLE IF NOT EXISTS FORMA_PAGO(
    ID_FORMA_PAGO VARCHAR(10) PRIMARY KEY,
    NOMBRE_FORMA_PAGO VARCHAR(100)
) ENGINE=InnoDB;

-- FACTURA_VENTA
CREATE TABLE IF NOT EXISTS FACTURA_VENTA(
    NUMERO_FACTURA INT AUTO_INCREMENT PRIMARY KEY,
    CEDULA_CLIENTE VARCHAR(20) NOT NULL,
    CODIGO_EMPLEADO CHAR(5) NOT NULL,
    FECHA_CREACION DATE,
    CONSTRAINT FK_FACTURA_CLIENTE FOREIGN KEY(CEDULA_CLIENTE) REFERENCES CLIENTE(CEDULA_CLIENTE),
    CONSTRAINT FK_FACTURA_EMPLEADO FOREIGN KEY(CODIGO_EMPLEADO) REFERENCES EMPLEADO(CODIGO_EMPLEADO)
) ENGINE=InnoDB;

-- PRODUCTO
CREATE TABLE IF NOT EXISTS PRODUCTO(
    ID_PRODUCTO VARCHAR(10) PRIMARY KEY,
    ID_TIPO_PRODUCTO VARCHAR(10) NOT NULL,
    DESCRIPCION VARCHAR(100),
    MARCA_PRODUCTO VARCHAR(100),
    CANTIDAD_STOCK INT NOT NULL CHECK(CANTIDAD_STOCK>=0),
    VALOR_UNITARIO DECIMAL(10,2) NOT NULL,
    IVA DECIMAL(4,2) NOT NULL CHECK (IVA>=0),
    CONSTRAINT FK_PRODUCTO_TIPO FOREIGN KEY(ID_TIPO_PRODUCTO) REFERENCES TIPO_PRODUCTO(ID_TIPO_PRODUCTO)
) ENGINE=InnoDB;

-- CONTRATO
CREATE TABLE IF NOT EXISTS CONTRATO(
    NUMERO_CONTRATO VARCHAR(20) PRIMARY KEY,
    FECHA_REALIZACION DATE,
    FECHA_INICIO DATE,
    FECHA_FIN DATE,
    FECHA_TERMINACION DATE NULL,
    SALARIO DECIMAL(10,2) NOT NULL CHECK(SALARIO>=0),
    NUMERO_CARGO VARCHAR(10) NOT NULL,
    CODIGO_EMPLEADO CHAR(5) NOT NULL,
    CONSTRAINT FK_CONTRATO_CARGO FOREIGN KEY(NUMERO_CARGO) REFERENCES CARGO(NUMERO_CARGO),
    CONSTRAINT FK_CONTRATO_EMPLEADO FOREIGN KEY(CODIGO_EMPLEADO) REFERENCES EMPLEADO(CODIGO_EMPLEADO)
) ENGINE=InnoDB;

-- DETALLE_FACTURA
CREATE TABLE IF NOT EXISTS DETALLE_FACTURA(
    ID_DETALLE_FACTURA_VENTA INT AUTO_INCREMENT PRIMARY KEY,
    CANTIDAD INT NOT NULL CHECK (CANTIDAD>0),
    ID_PRODUCTO VARCHAR(10) NOT NULL,
    NUMERO_FACTURA INT NOT NULL,
    CONSTRAINT FK_DETALLE_PRODUCTO FOREIGN KEY(ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO),
    CONSTRAINT FK_DETALLE_FACTURA FOREIGN KEY(NUMERO_FACTURA) REFERENCES FACTURA_VENTA(NUMERO_FACTURA)
) ENGINE=InnoDB;

-- FACTURA_PAGO
CREATE TABLE IF NOT EXISTS FACTURA_PAGO(
    ID_FACTURA_PAGO INT AUTO_INCREMENT PRIMARY KEY,
    ID_FORMA_PAGO VARCHAR(10) NOT NULL,
    NUMERO_FACTURA INT NOT NULL,
    CONSTRAINT FK_PAGO_FORMA FOREIGN KEY(ID_FORMA_PAGO) REFERENCES FORMA_PAGO(ID_FORMA_PAGO),
    CONSTRAINT FK_PAGO_FACTURA FOREIGN KEY(NUMERO_FACTURA) REFERENCES FACTURA_VENTA(NUMERO_FACTURA)
) ENGINE=InnoDB;

-- CELULAR_EMPLEADO
CREATE TABLE IF NOT EXISTS CELULAR_EMPLEADO(
    ID_CELULAR_EMPLEADO INT AUTO_INCREMENT PRIMARY KEY,
    TIPO VARCHAR(20),
    NUMERO_CELULAR VARCHAR(15),
    CODIGO_EMPLEADO CHAR(5) NOT NULL,
    CONSTRAINT FK_CELULAR_EMPLEADO FOREIGN KEY(CODIGO_EMPLEADO) REFERENCES EMPLEADO(CODIGO_EMPLEADO)
) ENGINE=InnoDB;




