CREATE DATABASE IF NOT EXISTS BIBLIOTECA; 
USE BIBLIOTECA; 

-- DROP DATABASE BIBLIOTECA;

-- ==============================
-- CREAR TABLAS
-- ==============================

-- EDITORIAL
CREATE TABLE IF NOT EXISTS EDITORIAL (
    ID_EDITORIAL VARCHAR(100) PRIMARY KEY,
    NOMBRE_EDITORIAL VARCHAR(100) NOT NULL,
    DIRECCION VARCHAR(200),
    TELEFONO VARCHAR(15),
    PAIS VARCHAR(100)
);

-- BIBLIOTECA
CREATE TABLE IF NOT EXISTS BIBLIOTECA (
    ID_BIBLIOTECA INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL
);

-- INSTITUTO
CREATE TABLE IF NOT EXISTS INSTITUTO (
    ID_INSTITUTO INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL
);

-- CODIGO
CREATE TABLE IF NOT EXISTS CODIGO (
    ID_CODIGO INT AUTO_INCREMENT PRIMARY KEY,
    TEXTO_CODIGO VARCHAR(100) NOT NULL,
    TIPO_CODIGO VARCHAR(50) NOT NULL
);

-- AUTOR
CREATE TABLE IF NOT EXISTS AUTOR (
    ID_AUTOR INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    APELLIDO VARCHAR(100) NOT NULL,
    NACIONALIDAD VARCHAR(100)
);

-- LIBRO
CREATE TABLE IF NOT EXISTS LIBRO (
    CODIGO_INTERNACIONAL VARCHAR(13) PRIMARY KEY,
    TITULO VARCHAR(200) NOT NULL,
    ID_EDITORIAL VARCHAR(100),
    ANIO_PUBLICACION YEAR,

    FOREIGN KEY(ID_EDITORIAL) REFERENCES EDITORIAL(ID_EDITORIAL)
);

-- SEDE_BIBLIOTECA
CREATE TABLE IF NOT EXISTS SEDE_BIBLIOTECA (
    ID_SEDE_BIBLIOTECA INT AUTO_INCREMENT PRIMARY KEY,
    ID_BIBLIOTECA INT NOT NULL,
    DIRECCION VARCHAR(200) NOT NULL,
    TELEFONO VARCHAR(15),
    CIUDAD VARCHAR(100) NOT NULL,

    FOREIGN KEY(ID_BIBLIOTECA) REFERENCES BIBLIOTECA(ID_BIBLIOTECA)
);

-- SEDE_INSTITUTO
CREATE TABLE IF NOT EXISTS SEDE_INSTITUTO ( 
    ID_SEDE_INSTITUTO INT AUTO_INCREMENT PRIMARY KEY,
    ID_INSTITUTO INT NOT NULL,
    DIRECCION VARCHAR(200) NOT NULL,
    TELEFONO VARCHAR(15),
    CIUDAD VARCHAR(100) NOT NULL,

    FOREIGN KEY(ID_INSTITUTO) REFERENCES INSTITUTO(ID_INSTITUTO)
);

-- SEDE_BIBLIOTECA_LIBRO
CREATE TABLE IF NOT EXISTS SEDE_BIBLIOTECA_LIBRO (
    ID_SEDE_BIBLIOTECA INT NOT NULL,
    CODIGO_INTERNACIONAL VARCHAR(13) NOT NULL,
    CANTIDAD_COPIAS INT NOT NULL CHECK(CANTIDAD_COPIAS>=0),
    PRIMARY KEY(ID_SEDE_BIBLIOTECA, CODIGO_INTERNACIONAL),

    FOREIGN KEY(ID_SEDE_BIBLIOTECA) REFERENCES SEDE_BIBLIOTECA(ID_SEDE_BIBLIOTECA),
    FOREIGN KEY(CODIGO_INTERNACIONAL) REFERENCES LIBRO(CODIGO_INTERNACIONAL)
);

-- CONVENIO_BIBLIOTECA
CREATE TABLE IF NOT EXISTS CONVENIO_BIBLIOTECA (
    ID_BIBLIOTECA1 INT NOT NULL,
    ID_BIBLIOTECA2 INT NOT NULL,
    FECHA_CONVENIO DATE NOT NULL,
    PRIMARY KEY(ID_BIBLIOTECA1, ID_BIBLIOTECA2),
    FOREIGN KEY(ID_BIBLIOTECA1) REFERENCES BIBLIOTECA(ID_BIBLIOTECA),
    FOREIGN KEY(ID_BIBLIOTECA2) REFERENCES BIBLIOTECA(ID_BIBLIOTECA)
);

-- DESCRIPTOR
CREATE TABLE IF NOT EXISTS DESCRIPTOR (
    ID_DESCRIPTOR INT AUTO_INCREMENT PRIMARY KEY,
    FECHA_CREACION DATE NOT NULL,
    ID_CODIGO INT NOT NULL,

    FOREIGN KEY(ID_CODIGO) REFERENCES CODIGO(ID_CODIGO)
);

-- TERMINO
CREATE TABLE IF NOT EXISTS TERMINO (
    ID_TERMINO INT AUTO_INCREMENT PRIMARY KEY,
    TEXTO_TERMINO VARCHAR(200) NOT NULL,
    ID_DESCRIPTOR INT NOT NULL,

    FOREIGN KEY(ID_DESCRIPTOR) REFERENCES DESCRIPTOR(ID_DESCRIPTOR)
);

-- LIBRO_AUTOR
CREATE TABLE IF NOT EXISTS LIBRO_AUTOR (
    CODIGO_INTERNACIONAL VARCHAR(13) NOT NULL,
    ID_AUTOR INT NOT NULL,
    PRIMARY KEY(CODIGO_INTERNACIONAL, ID_AUTOR),

    FOREIGN KEY(CODIGO_INTERNACIONAL) REFERENCES LIBRO(CODIGO_INTERNACIONAL),
    FOREIGN KEY(ID_AUTOR) REFERENCES AUTOR(ID_AUTOR)
);

-- LIBRO_DESCRIPTOR
CREATE TABLE IF NOT EXISTS LIBRO_DESCRIPTOR (
    CODIGO_INTERNACIONAL VARCHAR(13) NOT NULL,
    ID_DESCRIPTOR INT NOT NULL,
    PRIMARY KEY(CODIGO_INTERNACIONAL, ID_DESCRIPTOR),

    FOREIGN KEY(CODIGO_INTERNACIONAL) REFERENCES LIBRO(CODIGO_INTERNACIONAL),
    FOREIGN KEY(ID_DESCRIPTOR) REFERENCES DESCRIPTOR(ID_DESCRIPTOR)
);

-- BIBLIOTECA_CODIGO
CREATE TABLE IF NOT EXISTS BIBLIOTECA_CODIGO (
    ID_BIBLIOTECA INT NOT NULL,
    ID_CODIGO INT NOT NULL,
    PRIMARY KEY(ID_BIBLIOTECA, ID_CODIGO),

    FOREIGN KEY(ID_BIBLIOTECA) REFERENCES BIBLIOTECA(ID_BIBLIOTECA),
    FOREIGN KEY(ID_CODIGO) REFERENCES CODIGO(ID_CODIGO)
);

-- PRESTAMO
CREATE TABLE IF NOT EXISTS PRESTAMO (
    ID_PRESTAMO INT AUTO_INCREMENT PRIMARY KEY,
    CODIGO_INTERNACIONAL VARCHAR(13) NOT NULL,
    ID_SEDE_BIBLIOTECA INT NOT NULL,
    ID_SEDE_INSTITUTO INT NOT NULL,
    FECHA_PRESTAMO DATE NOT NULL,
    FECHA_DEVOLUCION DATE,
    ESTADO_PRESTAMO VARCHAR(50) NOT NULL,
    CANTIDAD_PRESTADA INT NOT NULL CHECK(CANTIDAD_PRESTADA>0),

    FOREIGN KEY(CODIGO_INTERNACIONAL) REFERENCES LIBRO(CODIGO_INTERNACIONAL),
    FOREIGN KEY(ID_SEDE_BIBLIOTECA) REFERENCES SEDE_BIBLIOTECA(ID_SEDE_BIBLIOTECA),
    FOREIGN KEY(ID_SEDE_INSTITUTO) REFERENCES SEDE_INSTITUTO(ID_SEDE_INSTITUTO)
);





