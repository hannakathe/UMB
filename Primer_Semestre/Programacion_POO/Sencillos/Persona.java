
package Sencillos;

// Clase Persona
class Persona {
    // Atributos
    private String nombre;
    private int edad;
    
    // Métodos para establecer el nombre y la edad
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    // Métodos para obtener el nombre y la edad
    public String getNombre() {
        return nombre;
    }
    
    public int getEdad() {
        return edad;
    }
}


